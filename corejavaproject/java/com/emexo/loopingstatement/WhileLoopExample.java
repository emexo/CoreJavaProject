package com.emexo.loopingstatement;

public class WhileLoopExample {
    public static void main(String[] args) {

        int counter = 1;

        while (counter<=5){
            System.out.println(counter);
            counter++;
        }


        int i = 1;
        do {
            System.out.println(i);
            i++;
        } while (i<=5);

    }
}
